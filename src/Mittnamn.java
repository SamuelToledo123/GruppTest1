public class Mittnamn {

    String namn;
    String efterNamn;
    int ålder;

    public Mittnamn(String namn,String efterNamn,int ålder) {
        this.namn = namn;
        this.efterNamn = efterNamn;
        this.ålder = ålder;


    }

    public String getNamn() {
        return namn;
    }
    public String getEfterNamn() {
        return efterNamn;

    }
    public int getÅlder() {
        return ålder;
    }
}
